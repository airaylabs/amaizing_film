// Current language
let currentLang = localStorage.getItem('lang') || 'id';

// Get translation
function t(key) {
  return TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS['en']?.[key] || key;
}

// Get current language
function getLang() {
  return currentLang;
}

// Switch language
function switchLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  
  // Update language buttons
  document.getElementById('lang-id').className = `w-8 h-8 rounded-full flex items-center justify-center text-sm ${lang === 'id' ? 'bg-cyan-500/30 ring-2 ring-cyan-500' : 'hover:bg-cyan-500/20'}`;
  document.getElementById('lang-en').className = `w-8 h-8 rounded-full flex items-center justify-center text-sm ${lang === 'en' ? 'bg-cyan-500/30 ring-2 ring-cyan-500' : 'hover:bg-cyan-500/20'}`;
  
  // Re-render app if function exists
  if (typeof renderApp === 'function') {
    renderApp();
  }
}

// Initialize language buttons on load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getLang();
  const langBtn = document.getElementById('lang-' + lang);
  if (langBtn) {
    langBtn.classList.add('bg-cyan-500/30', 'ring-2', 'ring-cyan-500');
  }
});
